#include<iostream>
using namespace std;

int main(){
    double a,b;
    cout<<"Enter two numbers a and b : ";
    cin>>a>>b;

    cout<< a << " + " << b << " = " << a+b<<".\n";
    cout<< a << " - " << b << " = " << a-b<<".\n";
    cout<< a << " * " << b << " = " << a*b<<".\n";
    cout<< a << " / " << b << " = " << a/b<<".\n";
    return 0;
}